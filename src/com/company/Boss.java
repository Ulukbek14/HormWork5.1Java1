package com.company;

public class Boss {
    int health;
    int damage;
    String typeofDefense;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getFireShield() {
        return typeofDefense;
    }

    public void setFireShield(String fireShield) {
        typeofDefense = fireShield;
    }
}
